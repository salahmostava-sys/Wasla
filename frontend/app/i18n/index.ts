const buildTranslations = (locale: Locale): Record<string, string> => {
  const valueIndex = locale === 'ar' ? 1 : 2;
  return Object.fromEntries(TRANSLATION_ENTRIES.map(([key, ...values]) => [key, values[valueIndex - 1]]));
};

